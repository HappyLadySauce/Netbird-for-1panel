#!/bin/bash
# Generate NetBird config for 1Panel (external OpenResty, embedded Dex).
# Aligned with netbirdio/netbird v0.71.4 getting-started.sh (exposed ports mode).

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
BASE_DIR="$(dirname "${SCRIPT_DIR}")"
DATA_DIR="${BASE_DIR}/data"

log() { echo "[netbird-init] $*"; }
fail() { log "ERROR: $*"; exit 1; }

# 1Panel writes form values to .env in the app install directory before init runs.
# 1Panel 在安装前将表单变量写入应用目录下的 .env，init 必须先加载。
load_panel_env() {
    local f
    for f in "${BASE_DIR}/.env" "./.env" "${SCRIPT_DIR}/.env"; do
        if [[ -f "${f}" ]]; then
            set -a
            # shellcheck disable=SC1090
            source "${f}"
            set +a
            log "Loaded env from ${f}"
            return 0
        fi
    done
    return 1
}

rand_b64() { openssl rand -base64 32; }
rand_b64_nopad() { openssl rand -base64 32 | tr -d '='; }

validate_domain() {
    local d="${1:-}"
    [[ -n "$d" ]] || fail "NETBIRD_DOMAIN is required"
    [[ "$d" != "netbird.example.com" ]] || fail "NETBIRD_DOMAIN cannot be netbird.example.com"
    if [[ "$d" =~ ^https?:// ]]; then
        fail "NETBIRD_DOMAIN must be hostname only (no http://)"
    fi
}

validate_port() {
    local name="$1" val="$2"
    [[ "$val" =~ ^[0-9]+$ ]] || fail "${name} must be a number"
    (( val >= 1 && val <= 65535 )) || fail "${name} must be between 1 and 65535"
}

load_panel_env || true

NETBIRD_DOMAIN="${NETBIRD_DOMAIN:-}"
PANEL_APP_PORT_HTTP="${PANEL_APP_PORT_HTTP:-8080}"
NETBIRD_MGMT_PORT="${NETBIRD_MGMT_PORT:-8081}"
NETBIRD_STUN_PORT="${NETBIRD_STUN_PORT:-3478}"
DATASTORE_ENCRYPTION_KEY="${DATASTORE_ENCRYPTION_KEY:-}"
NETBIRD_RELAY_AUTH_SECRET="${NETBIRD_RELAY_AUTH_SECRET:-}"

if [[ -z "${NETBIRD_DOMAIN}" ]]; then
    fail "NETBIRD_DOMAIN is required. Fill in the domain on the install form (hostname only, e.g. netbird.example.com)."
fi

validate_domain "$NETBIRD_DOMAIN"
validate_port "PANEL_APP_PORT_HTTP" "$PANEL_APP_PORT_HTTP"
validate_port "NETBIRD_MGMT_PORT" "$NETBIRD_MGMT_PORT"
validate_port "NETBIRD_STUN_PORT" "$NETBIRD_STUN_PORT"

[[ -n "$DATASTORE_ENCRYPTION_KEY" ]] || DATASTORE_ENCRYPTION_KEY="$(rand_b64)"
[[ -n "$NETBIRD_RELAY_AUTH_SECRET" ]] || NETBIRD_RELAY_AUTH_SECRET="$(rand_b64_nopad)"

NETBIRD_HTTP_PROTOCOL="https"
NETBIRD_PORT="443"
TRUSTED_PROXY="127.0.0.1/32"

mkdir -p "${DATA_DIR}/netbird"

cat > "${DATA_DIR}/config.yaml" <<EOF
# Combined NetBird Server Configuration
# Generated by 1Panel netbird app init.sh (v0.71.4)

server:
  listenAddress: ":80"
  exposedAddress: "${NETBIRD_HTTP_PROTOCOL}://${NETBIRD_DOMAIN}:${NETBIRD_PORT}"
  stunPorts:
    - ${NETBIRD_STUN_PORT}
  metricsPort: 9090
  healthcheckAddress: ":9000"
  logLevel: "info"
  logFile: "console"

  authSecret: "${NETBIRD_RELAY_AUTH_SECRET}"
  dataDir: "/var/lib/netbird"

  auth:
    issuer: "${NETBIRD_HTTP_PROTOCOL}://${NETBIRD_DOMAIN}/oauth2"
    signKeyRefreshEnabled: true
    dashboardRedirectURIs:
      - "${NETBIRD_HTTP_PROTOCOL}://${NETBIRD_DOMAIN}/nb-auth"
      - "${NETBIRD_HTTP_PROTOCOL}://${NETBIRD_DOMAIN}/nb-silent-auth"
    cliRedirectURIs:
      - "http://localhost:53000/"

  reverseProxy:
    trustedHTTPProxies:
      - "${TRUSTED_PROXY}"

  store:
    engine: "sqlite"
    encryptionKey: "${DATASTORE_ENCRYPTION_KEY}"
EOF

cat > "${DATA_DIR}/dashboard.env" <<EOF
NETBIRD_MGMT_API_ENDPOINT=${NETBIRD_HTTP_PROTOCOL}://${NETBIRD_DOMAIN}
NETBIRD_MGMT_GRPC_API_ENDPOINT=${NETBIRD_HTTP_PROTOCOL}://${NETBIRD_DOMAIN}
AUTH_AUDIENCE=netbird-dashboard
AUTH_CLIENT_ID=netbird-dashboard
AUTH_CLIENT_SECRET=
AUTH_AUTHORITY=${NETBIRD_HTTP_PROTOCOL}://${NETBIRD_DOMAIN}/oauth2
USE_AUTH0=false
AUTH_SUPPORTED_SCOPES=openid profile email groups
AUTH_REDIRECT_URI=/nb-auth
AUTH_SILENT_REDIRECT_URI=/nb-silent-auth
NGINX_SSL_PORT=443
LETSENCRYPT_DOMAIN=none
EOF

# OpenResty snippet for operator (paths use install-time ports)
cat > "${DATA_DIR}/openresty-snippet.conf" <<EOF
# NetBird OpenResty snippet — paste into 1Panel site custom config for ${NETBIRD_DOMAIN}
# Dashboard: 127.0.0.1:${PANEL_APP_PORT_HTTP}  |  Server: 127.0.0.1:${NETBIRD_MGMT_PORT}

upstream netbird_dashboard {
    server 127.0.0.1:${PANEL_APP_PORT_HTTP};
    keepalive 10;
}
upstream netbird_server {
    server 127.0.0.1:${NETBIRD_MGMT_PORT};
    keepalive 10;
}

location ~ ^/(relay|ws-proxy/) {
    proxy_pass http://netbird_server;
    proxy_http_version 1.1;
    proxy_set_header Upgrade \$http_upgrade;
    proxy_set_header Connection "Upgrade";
    proxy_set_header Host \$host;
    proxy_set_header X-Real-IP \$remote_addr;
    proxy_set_header X-Forwarded-For \$proxy_add_x_forwarded_for;
    proxy_set_header X-Forwarded-Proto \$scheme;
    proxy_read_timeout 1d;
}

location ~ ^/(signalexchange\.SignalExchange|management\.ManagementService)/ {
    grpc_pass grpc://netbird_server;
    grpc_set_header Host \$host;
    grpc_set_header X-Forwarded-For \$proxy_add_x_forwarded_for;
    grpc_read_timeout 1d;
    grpc_send_timeout 1d;
    grpc_socket_keepalive on;
}

location ~ ^/(api|oauth2)/ {
    proxy_pass http://netbird_server;
    proxy_set_header Host \$host;
    proxy_set_header X-Real-IP \$remote_addr;
    proxy_set_header X-Forwarded-For \$proxy_add_x_forwarded_for;
    proxy_set_header X-Forwarded-Proto \$scheme;
}

location / {
    proxy_pass http://netbird_dashboard;
    proxy_set_header Host \$host;
    proxy_set_header X-Real-IP \$remote_addr;
    proxy_set_header X-Forwarded-For \$proxy_add_x_forwarded_for;
    proxy_set_header X-Forwarded-Proto \$scheme;
}
EOF

chmod 600 "${DATA_DIR}/config.yaml" "${DATA_DIR}/dashboard.env" 2>/dev/null || true

log "Wrote ${DATA_DIR}/config.yaml and dashboard.env"
log "OpenResty snippet: ${DATA_DIR}/openresty-snippet.conf"
log "After install: configure 1Panel website HTTPS for ${NETBIRD_DOMAIN}, then visit https://${NETBIRD_DOMAIN}/setup"

if command -v curl >/dev/null 2>&1; then
    if curl -sf --max-time 3 "http://127.0.0.1:${NETBIRD_MGMT_PORT}/oauth2/.well-known/openid-configuration" >/dev/null 2>&1; then
        log "Management OIDC endpoint is reachable on port ${NETBIRD_MGMT_PORT}"
    fi
fi

exit 0
